def draw_sprites(sprite,x,y,cs=10,col=(0,0,0)): # cell size
  for i in range(len(sprite)):
    for k in range(len(sprite[i])):
      if sprite[i][k]==1:
        fill_rect(x+k*cs,y+i*cs,cs,cs,col)
def toupper(text):
  text_maj=""
  for letter in text:
    if ord(letter)>=97 and ord(letter)<=122:
      text_maj+=chr(ord(letter)-32)
      continue
    text_maj+=letter
  return text_maj
def sprite_string(text,x,y,size=20,col=(0,0,0)):
  text=toupper(text)
  for i in range(len(text)):
    draw_sprites(globals()[text[i]],x+int(size/5)*6*i,y,int(size/5),col)
  
